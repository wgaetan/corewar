#include <stdlib.h>
#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list *list;
	t_list *tmp;

	list = *alst;
	while (list)
	{
		tmp = list;
		list = list->next;
		ft_lstdelone(&tmp, del);
	}
	*alst = NULL;
}
