#include <stdlib.h>

void	*ft_memalloc(size_t size)
{
	char	*fresh;
	size_t	i;

	if (!(fresh = (char *)malloc(size)))
		return (NULL);
	i = 0;
	while (i < size)
	{
		fresh[i] = 0;
		i++;
	}
	return (fresh);
}
