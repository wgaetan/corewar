#include <stdlib.h>
#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char		*pd;
	const unsigned char	*ps;

	pd = dst;
	ps = src;
	if (ps < pd)
	{
		while (len--)
		{
			*(pd + len) = *(ps + len);
		}
	}
	else
		dst = ft_memcpy(dst, src, len);
	return (dst);
}
