#include "libft.h"

void	ft_putwc(const wchar_t c)
{
	unsigned char a;

	if (c >= 0x10000 && c <= 0x10ffff)
	{
		a = 0xf0 | (c >> 18);
		write(1, &a, 1);
	}
	if (c >= 0x0800)
	{
		a = (c >= 0x10000) ? 0x80 | ((c & 0x3f000) >> 12) : 0xe0 | (c >> 12);
		write(1, &a, 1);
	}
	if (c >= 0x0080)
	{
		a = (c >= 0x0800) ?
			0x80 | ((c & 0xfc0) >> 6) : 0xc0 | ((c & 0x7c0) >> 6);
		write(1, &a, 1);
	}
	a = (c >= 0x0080) ? 0x80 | (c & 0x3f) : c;
	write(1, &a, 1);
}
