#include "libft.h"

int		ft_strisnum(char *str)
{
	char *c;

	c = str;
	while (c && *c)
	{
		if (!ft_isdigit(*c) && !(c == str && (*c == '-' || *c == '+')))
			return (0);
		c++;
	}
	return (1);
}
