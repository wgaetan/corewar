#include <string.h>
#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t len;

	len = ft_strlen(s);
	while (len != 0)
	{
		if (*(s + len) == (unsigned char)c)
			return ((char *)(s + len));
		len--;
	}
	if (*s == (unsigned char)c)
		return ((char *)s);
	return (NULL);
}
