#include "libft.h"
#include <string.h>

char	*ft_strrev(char *str)
{
	size_t		i;
	size_t		len;
	char		tmp;

	i = 0;
	len = ft_strlen(str);
	while (i < len / 2)
	{
		tmp = str[i];
		str[i] = str[len - 1 - i];
		str[len - 1 - i] = tmp;
		i++;
	}
	str[len] = '\0';
	return (str);
}
