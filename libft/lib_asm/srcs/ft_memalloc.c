#include "../includes/libft.h"

void	*ft_memalloc(size_t size)
{
	void	*ret;
	size_t	i;

	if (!(ret = malloc(sizeof(*ret) * size)))
		return (NULL);
	i = 0;
	ft_bzero(ret, size);
	return (ret);
}
