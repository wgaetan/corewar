#include "../includes/libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	t;

	t = (char)c;
	if (t == '\0')
		return ((char *)(s + ft_strlen(s)));
	while (*s)
	{
		if (*s == t)
			return ((char *)(s));
		s++;
	}
	return (NULL);
}
